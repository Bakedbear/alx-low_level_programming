#include "calc.h"

/**
 * op_add - returns sum of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_add - subtracts two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: result of subtraction of two integers
 */
int op_sub(int a, int b)
{
        return (a - b);
}

/**
 * op_add - returns product of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result of multiplication of two integers
 */
int op_mul(int a, int b)
{
        return (a * b);
}

/**
 * op_add - returns result of division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result of division of two integers
 */
int op_div(int a, int b)
{
        return (a / b);
}

/**
 * op_add - returns remainder of a divided by b
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder of division of a by b
 */
int op_mod(int a, int b)
{
        return (a % b);
}





